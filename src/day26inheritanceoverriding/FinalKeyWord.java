package day26inheritanceoverriding;

public class FinalKeyWord {
	
	

	/*
	 * 
	  INterview Questions very Common. 
	  - " final " key word can be used for 
	  		- Variables : You have to initialize the variable
	  					which means you cannnot update the value after initializing 
	  						
	  		- Methods : It means you cannot change the body of the method 
			- Class : Cannot have child classes, you cant extend to final classes
	 		- Final classes can be child but can not be parent classes 
	 		Note: All wrapper classes are final classes.
	 		     - All immutable classes are final classes, so String Class is a final class
	 		     

	 
	 */
	
	
	
	
	
	public final String name = " Ali Can";
	
	
	
	
	public static void main(String[] args) {
		FinalKeyWord obj = new FinalKeyWord();
	
		
		

	}

}
